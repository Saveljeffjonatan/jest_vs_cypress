const Addition = (input1: number, input2: number): any => {
  return input1 + input2;
};

const Subtraction = (input1: number, input2: number): any => {
  return input1 - input2;
};

const Multiplication = (input1: number, input2: number): any => {
  return input1 * input2;
};

export { Addition, Subtraction, Multiplication };
